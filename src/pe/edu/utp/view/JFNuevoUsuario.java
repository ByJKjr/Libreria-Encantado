private void agregarUsuario(){
  //OBTENER LOS DATOS
  String nombres,apellidoPaterno,apellidoMaterno,area,fechaContratacion,correo;
  int dni;
  nombres = txt_Nombres.getText();
  apellidoPaterno = txt_ApellidoPaterno.getText();
  apellidoMaterno = txt_ApellidoMaterno.getText();
  area = box_Area.getSelectedItem().toString();
  fechaContratacion = txt_FechaContratacion.getText();
  dni = Integer.parseInt(txt_Dni.getText());
  correo = txt_CorreoElectronico.getText();
  
  //AGREGAR USUARIO
  boolean confirmacion = objUsuarioControl.guardarUsuario(nombres, apellidoPaterno, apellidoMaterno, 
          area, fechaContratacion, dni, correo);
  
  //CONFIRMAR LA AGREGACION
   if(confirmacion){
       JOptionPane.showMessageDialog(null, "Se guardo correctamente");
       //LIMPIAR LOS TEXTFIELDS
       limpiar();
       //PILAS: AGREGAR AL HISTORIAL
       String fechaActual = LocalDate.now().toString();
       DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm:ss");
       String horaActual = LocalTime.now().format(formatter);
       String actividad = "Se agrego un nuevo usuario";
       objHistorialControl.agregarHistorial(fechaActual, horaActual, actividad);
   }else{
       JOptionPane.showMessageDialog(null, "Algo paso");
   }
  
}  
    
private void limpiar(){
    txt_Nombres.setText("");
    txt_ApellidoMaterno.setText("");
    txt_ApellidoPaterno.setText("");
    txt_CorreoElectronico.setText("");
    txt_Dni.setText("");
    txt_FechaContratacion.setText("yyyy-mm-dd");
    }
