    //CREAR TABLA
    private DefaultTableModel crearTabla() {
        DefaultTableModel objModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Todas las celdas no son editables
            }
        ;
        };
        String[] columna = {
            "ID", "Nombre Completo", "Telefono", "Direccion"
        };
        objModel.setColumnIdentifiers(columna);
        //Estableciendo el modelo al jtable
        //Colocando el inventario de la bd a cada fila de la tabla

        return objModel;
    }

    public void enviarDato(int id) {
        JFNuevoPedidoProducto objPedido = new JFNuevoPedidoProducto();
        objPedido.setLocationRelativeTo(null);
        objPedido.setVisible(true);
        objPedido.recibirDato(id);
        this.dispose();
    }

    public void actualizarTablaCliente() {
        List<Cliente> clientes = objClienteControl.obtenerTodosLosClientes();
        DefaultTableModel objModel = crearTabla();

        for (Cliente cliente : clientes) {
            objModel.addRow(new Object[]{
                cliente.getId_cliente(),
                cliente.toString(),
                cliente.getTelefono(),
                cliente.getDireccion()
            });
        }

        jTable1.setModel(objModel);
    }

    private void nuevoCliente() {
        JFNuevoCliente objNuevoCliente = new JFNuevoCliente();
        objNuevoCliente.setLocationRelativeTo(null);
        objNuevoCliente.setVisible(true);
    }

    private void elegirCliente() {
        int filaSeleccionada = jTable1.getSelectedRow();
        if (filaSeleccionada != -1) {
            // Obtener datos de la fila seleccionada
            id = (int) jTable1.getValueAt(filaSeleccionada, 0);
            enviarDato(id);
        } else {
            JOptionPane.showMessageDialog(null, "No se selecciono ningun Cliente");
        }
    }

