  //AGREGAR UN CLIENTE
 private void agregarCliente(){
   //OBTENER DATOS INGRESADOS
   String nombre,apellidoPa,apellidoMa,telefono,correoElectronico,direccion;
   nombre = txt_Nombres.getText().trim();
   apellidoPa = txt_ApellidoPaterno.getText().trim();
   apellidoMa = txt_ApellidoMaterno.getText().trim();
   telefono = txt_Telefono.getText().trim();
   correoElectronico = txt_Correo.getText().trim();
   direccion = txt_Direccion.getText().trim();
   
   //ENVIAR AL CONTROLLER PARA QUE LO PROCESE
   boolean confirmacion = objClienteControl.guardarCliente(nombre,apellidoPa,
           apellidoMa,correoElectronico,telefono,direccion);
   
   //CONFIRMAR LA AGREGACION
   if(confirmacion){
       JOptionPane.showMessageDialog(null, "Se guardo correctamente");
       //LIMPIAR LOS TEXTFIELDS
       limpiar();
       //PILAS: AGREGAR AL HISTORIAL
       String fechaActual = LocalDate.now().toString();
       DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm:ss");
       String horaActual = LocalTime.now().format(formatter);
       String actividad = "Se agrego un nuevo cliente";
       objHistorialControl.agregarHistorial(fechaActual, horaActual, actividad);
   }else{
       JOptionPane.showMessageDialog(null, "Algo paso");
   }
 }
 
